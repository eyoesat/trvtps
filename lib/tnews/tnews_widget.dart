    return Scaffold(
      key: scaffoldKey,
      appBar: AppBar(
        backgroundColor: Color(0xFF043C28),
        automaticallyImplyLeading: true,
        title: Text(
          'Announcement',
          style: FlutterFlowTheme.of(context).bodyText1.override(
